                    存在該數據，若是不存在(即沒有選項)則要返回空陣列。

                    isset($_POST["name"]) ? $_POST["name"] : array();

            4.PHP組織SQL直接存儲到資料庫中。


        二、多選按鈕的數據處理：資料庫 → PHP
            1.如果是反過來操作，從資料庫中取出數據後之後，使用explode()把字符串轉換
              成陣列。

            2.in_array()：
                在HTML顯示當中，通過判斷複選框元素是否在陣列中存在，來確認複選框checkbox
                是否有checked="checked" 屬性。

*/
