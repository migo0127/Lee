        一、多選按鈕的數據處理：PHP → 資料庫
            1.表單中name屬性使用陣列格式：屬性名[] (一類複選框數據使用同一個)。
            2.後台收到數據之後，是一個陣列形式，但陣列不能存袸到資料庫中。
